package leetcode_problem_2441_largest_positive_integer

import java.util.*
import kotlin.math.abs

class LargestPositiveIntegerThatExistNegativeValue {
    fun findMaxK(nums: IntArray): Int {
        Arrays.sort(nums)
        var left = 0
        var right = nums.size - 1
        while (left < right) {
            if (abs(nums[left]) > nums[right]) {
                left++
            } else if (abs(nums[left]) < nums[right]) {
                right--
            } else if (nums[left] + nums[right] == 0) {
                return nums[right]
            } else {
                left++
                right--
            }
        }
        return -1
    }
}

